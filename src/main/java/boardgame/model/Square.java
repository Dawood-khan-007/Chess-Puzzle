package boardgame.model;

/**
 * Enum representing the possible types of squares on a board game.
 */
public enum Square {
    /**
     * Represents an empty square.
     */
    NONE,

    /**
     * Represents a square with a king.
     */
    KING,

    /**
     * Represents a square with a rook.
     */
    ROOK,

    /**
     * Represents a square with a bishop.
     */
    BISHOP
}
